public class No6 {
    public static void main(String[] args) {
        int a = 2, b = 3;
        System.out.println("For positive value");
        System.out.println("Example of Bitwise OR = " + (a | b));
        System.out.println("Example of Bitwise AND = " + (a & b));
        System.out.println("Example of Bitwise XOR = " + (a ^ b));
        System.out.println("Example of Bitwise ~ = " + (~a));
        System.out.println("Example of Right Shift >> = " + (a >> 2));
        System.out.println("Example of Unsigned Right Shift >>> = " + (a >>> 2));
        System.out.println("Example of Left Shift << = " + (a << 2));
        int c = -2, d = -3;
        System.out.println("For negative value");
        System.out.println("Example of Bitwise OR = " + (c | d));
        System.out.println("Example of Bitwise AND = " + (c & d));
        System.out.println("Example of Bitwise XOR = " + (c ^ d));
        System.out.println("Example of Bitwise ~ = " + (~c));
        System.out.println("Example of Right Shift >> = " + (c >> 2));
        System.out.println("Example of Unsigned Right Shift >>> = " + (c >>> 2));
        System.out.println("Example of Left Shift << = " + (c << 2));

    }
}